function deleteSymbol() {
  const selected = document.querySelector('input[name="charr"]:checked').value;
  let txt = document.getElementById("text").innerHTML;
  let cnt = 0;
  while(cnt != txt.length) {
    txt = txt.replace(selected, "");
    cnt++;
  }
  const newTxt = document.getElementById("text");
  newTxt.innerHTML = txt;
  return false;
}
